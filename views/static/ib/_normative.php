            Защита информации
        </div>
        <div class="spoiler-body gallery" style="display: none;">
            <p><strong><em>Основные понятия:</em></strong></p>
            <p><strong>информация</strong> - сведения (сообщения, данные) независимо от формы их представления;</p>
            <p><strong>информационная система</strong> - совокупность содержащейся в базах данных информации и обеспечивающих ее обработку информационных технологий и технических средств;</p>
            <p><strong>средства защиты информации</strong> - технические, криптографические, программные и другие средства, предназначенные для защиты сведений, составляющих государственную тайну, средства, в которых они реализованы, а также средства контроля эффективности защиты информации;</p>
            <p>к <strong>служебной информации ограниченного распространения</strong> относится несекретная информация, касающаяся деятельности налоговых органов, ограничения на распространение которой диктуются служебной необходимостью.</p>
