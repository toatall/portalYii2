            <p><strong><em>Основные понятия:</em></strong></p>
            <p><strong>безопасность критической информационной инфраструктуры</strong> - состояние защищенности критической информационной инфраструктуры, обеспечивающее ее устойчивое функционирование при проведении в отношении ее компьютерных атак;</p>
            <p><strong>компьютерный инцидент</strong> - факт нарушения и (или) прекращения функционирования объекта критической информационной инфраструктуры, сети электросвязи, используемой для организации взаимодействия таких объектов, и (или) нарушения безопасности обрабатываемой таким объектом информации, в том числе произошедший в результате компьютерной атаки;</p>
